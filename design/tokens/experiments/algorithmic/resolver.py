#!/usr/bin/env python3
"""
Algorithmic token resolver.

Reads base.tokens.json + semantic.tokens.json, resolves {base.*} and
{L.*} references, and emits tokens.css scoped under
[data-token-system="algorithmic"].

Reference substitution rules:
  {base.X}     → var(--base-X)           kept live in CSS (designer edits --base-* in DevTools)
  {base.Y} inside calc()/oklch()/clamp()  → same, var(--base-Y)

Mode overrides are generated from:
  - $extensions.com.opntrntbl.dark      → [data-color="dark"]
  - $extensions.com.opntrntbl.hc        → [data-contrast="enhanced"]
  - $extensions.com.opntrntbl.hc-dark   → [data-color="dark"][data-contrast="enhanced"]
  - base.density-factor.$extensions     → [data-density="compact/spacious"]
"""

import json, re
from pathlib import Path

ROOT = Path(__file__).parent
SRC  = ROOT / "src"

def load(path):
    with open(path) as f:
        return json.load(f)

def token_to_css_var(path: str) -> str:
    """semantic.surface.canvas → --surface-canvas"""
    return "--" + path.replace(".", "-")

def base_key_to_css_var(key: str) -> str:
    """base.space-unit → --base-space-unit"""
    return "--" + key.replace(".", "-")

def resolve_refs(value: str, base_flat: dict) -> str:
    """Replace {base.X} → var(--base-X) in a string.
    If a CSS unit (px, ms, %) immediately follows the reference,
    wrap as (var(--base-X) * 1unit) so it's valid inside calc()."""
    def sub(m):
        ref  = m.group(1)
        unit = m.group(2) or ""
        var  = f"var({base_key_to_css_var(ref)})"
        return f"({var} * 1{unit})" if unit else var
    return re.sub(r"\{([^}]+)\}(px|em|rem|ms|s)?", sub, value)

def flatten(obj: dict, prefix: str = "") -> dict:
    """Flatten nested dict into dot-path → value."""
    result = {}
    for k, v in obj.items():
        if k.startswith("$"):
            continue
        path = f"{prefix}.{k}" if prefix else k
        if isinstance(v, dict) and "$value" not in v:
            result.update(flatten(v, path))
        else:
            result[path] = v
    return result

def value_to_css(val) -> str:
    """Convert a $value to a CSS string."""
    if isinstance(val, str):
        return val
    if isinstance(val, (int, float)):
        return str(val)
    if isinstance(val, list):
        return ", ".join(f'"{f}"' if " " in f else f for f in val)
    if isinstance(val, dict):
        return None  # composite — handled separately
    return str(val)

def emit_typography(path: str, val: dict, base_flat: dict) -> list[str]:
    """Expand a typography composite token into 4 CSS vars."""
    lines = []
    css_base = token_to_css_var(path)
    props = {
        "fontSize":    "size",
        "lineHeight":  "line-height",
        "fontWeight":  "weight",
        "fontFamily":  "family",
    }
    for key, suffix in props.items():
        if key in val:
            raw = value_to_css(val[key])
            resolved = resolve_refs(raw, base_flat)
            lines.append(f"  {css_base}-{suffix}: {resolved};")
    return lines

def emit_transition(path: str, val: dict, base_flat: dict) -> list[str]:
    lines = []
    css_base = token_to_css_var(path)
    if "duration" in val:
        dur = resolve_refs(value_to_css(val["duration"]), base_flat)
        lines.append(f"  {css_base}-duration: {dur};")
    if "timingFunction" in val:
        lines.append(f"  {css_base}-easing: {val['timingFunction']};")
    return lines

def generate():
    base_data = load(SRC / "base.tokens.json")
    sem_data  = load(SRC / "semantic.tokens.json")

    base_flat = flatten(base_data)   # "base.hue" → {"$type":..., "$value":..., ...}
    sem_flat  = flatten(sem_data)    # "surface.canvas" → {...}

    scope = '[data-token-system="algorithmic"]'
    out = [
        "/*",
        " * Algorithmic token system — generated by resolver.py",
        " * Do not edit directly. Edit src/base.tokens.json or src/semantic.tokens.json.",
        " *",
        " * Architecture:",
        " *   {base.*} references stay as var(--base-*) in CSS so they're live in DevTools.",
        " *   Changing --base-hue in DevTools repaints all tinted surfaces instantly.",
        " *   Changing --base-font-size rescales the entire type system.",
        " */",
        "",
    ]

    # ── :root base vars (used outside the scope for inheritance) ──────────────
    out.append("/* Base parameters — override these to retheme */")
    out.append(f"{scope} {{")

    for path, tok in sorted(base_flat.items()):
        val = tok.get("$value")
        if val is None:
            continue
        typ = tok.get("$type", "")
        css_var = base_key_to_css_var(path)

        if typ == "fontFamily" and isinstance(val, list):
            families = ", ".join(f'"{f}"' if " " in f else f for f in val)
            out.append(f"  {css_var}: {families};")
        elif isinstance(val, (int, float)):
            out.append(f"  {css_var}: {val};")
        elif isinstance(val, str):
            out.append(f"  {css_var}: {val};")

    out.append("")

    # ── semantic vars (default / light) ───────────────────────────────────────
    out.append("  /* Semantic tokens — light default */")
    for path, tok in sorted(sem_flat.items()):
        val = tok.get("$value")
        if val is None:
            continue
        typ = tok.get("$type", "")
        css_var = token_to_css_var(path)

        if typ == "typography" and isinstance(val, dict):
            out.extend(emit_typography(path, val, base_flat))
        elif typ == "transition" and isinstance(val, dict):
            out.extend(emit_transition(path, val, base_flat))
        else:
            raw = value_to_css(val)
            if raw is None:
                continue
            resolved = resolve_refs(raw, base_flat)
            out.append(f"  {css_var}: {resolved};")

    out.append("}")
    out.append("")

    # ── dark mode overrides ────────────────────────────────────────────────────
    dark_lines = []
    for path, tok in sorted(sem_flat.items()):
        ext = tok.get("$extensions", {})
        dark_val = ext.get("com.opntrntbl.dark")
        if dark_val:
            css_var  = token_to_css_var(path)
            resolved = resolve_refs(value_to_css(dark_val), base_flat)
            dark_lines.append(f"  {css_var}: {resolved};")

    if dark_lines:
        out.append(f'{scope}[data-color="dark"] {{')
        out.extend(dark_lines)
        out.append("}")
        out.append("")

    # ── HC overrides ──────────────────────────────────────────────────────────
    hc_lines = []
    for path, tok in sorted(sem_flat.items()):
        ext = tok.get("$extensions", {})
        hc_val = ext.get("com.opntrntbl.hc")
        if hc_val:
            css_var  = token_to_css_var(path)
            resolved = resolve_refs(value_to_css(hc_val), base_flat)
            hc_lines.append(f"  {css_var}: {resolved};")

    if hc_lines:
        out.append(f'{scope}[data-contrast="enhanced"] {{')
        out.extend(hc_lines)
        out.append("}")
        out.append("")

    # ── HC + dark overrides ────────────────────────────────────────────────────
    hc_dark_lines = []
    for path, tok in sorted(sem_flat.items()):
        ext = tok.get("$extensions", {})
        hc_dark_val = ext.get("com.opntrntbl.hc-dark")
        if hc_dark_val:
            css_var  = token_to_css_var(path)
            resolved = resolve_refs(value_to_css(hc_dark_val), base_flat)
            hc_dark_lines.append(f"  {css_var}: {resolved};")

    if hc_dark_lines:
        out.append(f'{scope}[data-color="dark"][data-contrast="enhanced"] {{')
        out.extend(hc_dark_lines)
        out.append("}")
        out.append("")

    # ── density overrides ─────────────────────────────────────────────────────
    density_tok = base_flat.get("base.density-factor", {})
    density_ext = density_tok.get("$extensions", {})
    compact_val  = density_ext.get("com.opntrntbl.compact")
    spacious_val = density_ext.get("com.opntrntbl.spacious")

    if compact_val is not None:
        out.append(f'{scope}[data-density="compact"] {{')
        out.append(f"  --base-density-factor: {compact_val};")
        out.append("}")
        out.append("")

    if spacious_val is not None:
        out.append(f'{scope}[data-density="spacious"] {{')
        out.append(f"  --base-density-factor: {spacious_val};")
        out.append("}")
        out.append("")

    # Vision overrides — override base hue/chroma params so the entire system recalculates
    out.append(f'/* ── Vision — achromatopsia (no color) ─────────────────────────────────── */')
    out.append(f'{scope}[data-vision="achromatopsia"] {{')
    out.append( '  --base-chroma: 0;')
    out.append( '  --base-chroma-accent: 0;')
    out.append( '  --base-chroma-status: 0;')
    out.append('}')
    out.append(f'{scope}[data-color="dark"][data-vision="achromatopsia"] {{')
    out.append( '  --base-chroma: 0;')
    out.append( '  --base-chroma-accent: 0;')
    out.append( '  --base-chroma-status: 0;')
    out.append('}')
    out.append('')

    out.append(f'/* ── Vision — deuteranopia (no green) ──────────────────────────────────── */')
    out.append(f'{scope}[data-vision="deuteranopia"] {{')
    out.append( '  --base-hue-success: 195;')
    out.append( '  --base-hue-warning: 100;')
    out.append('}')
    out.append(f'{scope}[data-color="dark"][data-vision="deuteranopia"] {{')
    out.append( '  --base-hue-success: 195;')
    out.append( '  --base-hue-warning: 100;')
    out.append('}')
    out.append('')

    out.append(f'/* ── Vision — protanopia (no red) ──────────────────────────────────────── */')
    out.append(f'{scope}[data-vision="protanopia"] {{')
    out.append( '  --base-hue-danger: 300;')
    out.append( '  --base-hue-success: 255;')
    out.append( '  --base-hue-info: 195;')
    out.append('}')
    out.append(f'{scope}[data-color="dark"][data-vision="protanopia"] {{')
    out.append( '  --base-hue-danger: 300;')
    out.append( '  --base-hue-success: 255;')
    out.append( '  --base-hue-info: 195;')
    out.append('}')
    out.append('')

    out.append(f'/* ── Vision — tritanopia (no blue) ─────────────────────────────────────── */')
    out.append(f'{scope}[data-vision="tritanopia"] {{')
    out.append( '  --base-hue-info: 300;')
    out.append( '  --base-hue-accent: 55;')
    out.append('}')
    out.append(f'{scope}[data-color="dark"][data-vision="tritanopia"] {{')
    out.append( '  --base-hue-info: 300;')
    out.append( '  --base-hue-accent: 55;')
    out.append('}')
    out.append('')

    css = "\n".join(out)
    (ROOT / "tokens.css").write_text(css)
    print(f"  Wrote {ROOT / 'tokens.css'} ({len(out)} lines)")

if __name__ == "__main__":
    generate()
