#!/usr/bin/env python3
"""
Modern (flat-ui) nested-modes token resolver.

Architecture: separate source files per mode layer, no inlining.
  src/defaults.tokens.json          → [data-token-system="modern-nested"] {}
  src/modes/color.dark.tokens.json  → [data-token-system="modern-nested"][data-color="dark"] {}
  src/modes/color.hc.tokens.json    → [data-token-system="modern-nested"][data-contrast="enhanced"] {}
  src/modes/density.compact.tokens.json  → [data-token-system="modern-nested"][data-density="compact"] {}
  src/modes/density.spacious.tokens.json → [data-token-system="modern-nested"][data-density="spacious"] {}
  src/modes/vision.*.tokens.json    → [data-token-system="modern-nested"][data-vision="*"] {}

Mode combinations (dark + compact, dark + hc, etc.) are NOT declared.
CSS cascade resolves them. This is O(n + m) declarations, not O(n × m).

Identity: modern (ShadCN/Aceternity-inspired) — white cards on zinc-50 canvas.
Depth = 1px border + shadow-sm. Radius: 4-6px.
"""

import json, re
import importlib.util as _ilu
from pathlib import Path

ROOT  = Path(__file__).parent
SRC   = ROOT / "src"
MODES = SRC / "modes"

SCOPE_NAME = "modern-nested"

def load(path):
    with open(path) as f:
        return json.load(f)

def flatten(obj: dict, prefix: str = "") -> dict:
    result = {}
    for k, v in obj.items():
        if k.startswith("$"):
            continue
        path = f"{prefix}.{k}" if prefix else k
        if isinstance(v, dict) and "$value" not in v:
            result.update(flatten(v, path))
        else:
            result[path] = v
    return result

def token_path_to_css_var(path: str) -> str:
    return "--" + path.replace(".", "-")

def emit_block(tokens_flat: dict, indent: str = "  ") -> list:
    lines = []
    for path, tok in sorted(tokens_flat.items()):
        val = tok.get("$value") if isinstance(tok, dict) else tok
        typ = tok.get("$type", "") if isinstance(tok, dict) else ""
        css_var = token_path_to_css_var(path)

        if isinstance(val, (int, float)) and not isinstance(val, bool):
            lines.append(f"{indent}{css_var}: {val};")
        elif typ == "cubicBezier" and isinstance(val, list) and len(val) == 4:
            css_val = f"cubic-bezier({', '.join(str(v) for v in val)})"
            lines.append(f"{indent}{css_var}: {css_val};")
        elif isinstance(val, list):
            parts = []
            for item in val:
                if isinstance(item, str):
                    parts.append(f'"{item}"' if " " in item else item)
                else:
                    parts.append(str(item))
            lines.append(f"{indent}{css_var}: {', '.join(parts)};")
        else:
            lines.append(f"{indent}{css_var}: {val};")
    return lines

def generate():
    scope = f'[data-token-system="{SCOPE_NAME}"]'

    defaults = flatten(load(SRC / "defaults.tokens.json"))

    mode_files = {
        '[data-color="dark"]':                         "color.dark.tokens.json",
        '[data-contrast="enhanced"]':                  "color.hc.tokens.json",
        '[data-density="compact"]':                    "density.compact.tokens.json",
        '[data-density="spacious"]':                   "density.spacious.tokens.json",
        '[data-vision="achromatopsia"]':               "vision.achromatopsia.tokens.json",
        '[data-vision="deuteranopia"]':                "vision.deuteranopia.tokens.json",
        '[data-vision="protanopia"]':                  "vision.protanopia.tokens.json",
        '[data-vision="tritanopia"]':                  "vision.tritanopia.tokens.json",
    }

    out = [
        "/*",
        f" * Modern — Nested-modes token system — generated by resolver.py",
        f" * Scope: {scope}",
        " * Do not edit directly. Edit src/defaults.tokens.json or src/modes/*.tokens.json.",
        " *",
        " * Architecture: each mode layer is a separate file with only its overrides.",
        " * Combinations (dark + compact, dark + hc, etc.) are NOT declared —",
        " * the CSS cascade resolves them by stacking the active [data-*] selectors.",
        " * This is O(n + m) declarations, not O(n × m).",
        " */",
        "",
        f"{scope} {{",
    ]
    out.extend(emit_block(defaults))
    out.append("}")
    out.append("")

    for selector, fname in mode_files.items():
        fpath = MODES / fname
        if not fpath.exists():
            continue
        mode_tokens = flatten(load(fpath))
        out.append(f"{scope}{selector} {{")
        out.extend(emit_block(mode_tokens))
        out.append("}")
        out.append("")

    # Component overrides — import from flat-ui resolver, re-scope
    _spec = _ilu.spec_from_file_location("_src", Path(__file__).parent.parent / "flat-ui" / "resolver.py")
    _mod = _ilu.module_from_spec(_spec)
    _spec.loader.exec_module(_mod)
    _overrides = _mod.COMPONENT_OVERRIDES.replace(
        '[data-token-system="flat-ui"]',
        f'[data-token-system="{SCOPE_NAME}"]'
    )
    out.append(_overrides)

    css = "\n".join(out)
    (ROOT / "tokens.css").write_text(css)
    print(f"  Wrote {ROOT / 'tokens.css'} ({len(out)} lines)")

if __name__ == "__main__":
    generate()
